// Create a script that reads the contents of input.txt.
// o Convert it to uppercase.
// o Write the result into a new file called uppercaseOutput.txt.
// o Print a message to confirm success.

const fs = require("fs").promises;

async function makeFileCapital() {
    try {
        const data = await fs.readFile("input.txt", "utf-8"); // Waits here
        const capitalData = data.toUpperCase();
        
        await fs.writeFile("input.txt", capitalData); // Waits here
        console.log("Success!");
    } catch (err) {
        console.error("Error:", err);
    }
}

makeFileCapital();