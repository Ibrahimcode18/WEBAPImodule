// file: appendExample.js
const fs = require("fs");
const newLine = "\nAdditional line of text.";
// Append text to the end of a file
fs.appendFile("output.txt", newLine, (err) => {
 if (err) {
 console.error("Error appending to file:", err);
 return;
  }
 console.log("Data appended successfully!");
});