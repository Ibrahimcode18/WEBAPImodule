// Looping Exercise
// Create a for loop to print the numbers from 10 down to 1.

for (let i = 10; i > 0; i--){
    console.log(i);
}
// Then rewrite the same logic using a while loop.
let max = 10
while (max > 0){
    console.log(max);
    max--
}
